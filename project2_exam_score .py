import pandas as pd
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_squared_error,mean_absolute_error, r2_score
import matplotlib.pyplot as plt
import numpy as np

data=pd.read_csv("student_exam_scores.csv")
print(data['hours_studied'].head())
print(data['exam_score'].head())
X=data[['hours_studied']]
y=data['exam_score']
model=LinearRegression()
model.fit(X,y)
predicted_score=model.predict(X)
mae=mean_absolute_error(y,predicted_score)
mse=mean_squared_error(y,predicted_score)
rmse=np.sqrt(mse)
r2=r2_score(y,predicted_score)
print("Mean absolute error", round(mae,2))
print("Mean squared  error", round(mse,2))
print(" Root Mean Squared error", round(rmse,2))
print(" R^2 score ", round(r2,2))

#histogram
plt.figure(figsize=(8,6))
plt.hist(y,bins=30,color='skyblue', edgecolor='black')
plt.title("Distribution of final score of exam ")
plt.xlabel(" exam scores")
plt.ylabel(" no of students  ")

#scatter plot
plt.figure(figsize=(8,6))
plt.scatter(X,y,color='skyblue', label='Actual Score')
plt.plot(X,predicted_score,color='red',label='predicted_scores(regression line)')
plt.title("Model prediction vs actual score  ")
plt.xlabel(" study hour ")
plt.ylabel(" final output   ")
plt.grid(True)
plt.show()

